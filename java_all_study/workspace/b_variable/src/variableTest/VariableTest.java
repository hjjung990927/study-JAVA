package variableTest;

public class VariableTest {
	public static void main(String[] args) {
		int a = 10;
//		변수 a 를 10, 정수형으로 선언한다.
		float b = 10.1563F;
//		변수 b 를 10.1563, float 실수형으로 선언한다.
//		하지만 실수형은 double 이 기본형으로 사용되어 값 뒤에 F를 꼭 붙인다.
		double c = 10.1563;
//		변수 c 를 10.1563, double 실수형으로 선언한다.
		char d = 'A';
//		변수 d 를 A, char 문자형으로 선언한다.
		String e = "ABC";
//		변수 e 를 ABC, String 문자열형으로 선언한다.
		
		System.out.println(a);
//		변수 a 를 출력한다.
		System.out.println(b);
//		변수 b 를 출력한다.
		System.out.println(c);
//		변수 c 를 출력한다.
		System.out.println(d);
//		변수 d 를 출력한다.
		System.out.println(e);
//		변수 e 를 출력한다.
	}
}

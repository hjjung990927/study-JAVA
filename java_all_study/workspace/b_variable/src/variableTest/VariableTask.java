package variableTest;

public class VariableTask {
	public static void main(String[] args) {
		String name = "정희준";
//		변수 name 에 문자열 정희준을 선언한다.
		int age = 27;
//		변수 age 에 정수형 27을 선언한다.
		double height = 172.0;
//		변수 height 에 실수형 172.0을 선언한다.
		String hobby = "야구관람";
//		변수 hobby 에 문자열 야구관람을 선언한다.
		
		System.out.println(name);
//		변수 name 을 출력한다.
		System.out.println(age);
//		변수 age 를 출력한다.
		System.out.println(height);
//		변수 height 를 출력한다.
		System.out.println(hobby);
//		변수 hobby 를 출력한다.
	}
}
